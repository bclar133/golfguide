import fs from 'node:fs';

const endpoints = [
  'https://overpass-api.de/api/interpreter',
  'https://overpass.kumi.systems/api/interpreter'
];

const query = `[out:json][timeout:180];
area["ISO3166-1"="AU"][admin_level=2]->.australia;
(
  node["leisure"="golf_course"](area.australia);
  way["leisure"="golf_course"](area.australia);
  relation["leisure"="golf_course"](area.australia);
);
out center tags;`;

const fallbackCourses = [
  course('royal-melbourne-golf-club', 'Royal Melbourne Golf Club', 'Black Rock', 'Melbourne Sandbelt', 'VIC', 145.0287, -37.9697, '36', 'Private club', 5, 'https://www.royalmelbourne.com.au/'),
  course('barnbougle-dunes', 'Barnbougle Dunes', 'Bridport', 'Tasmania', 'TAS', 147.4385, -40.9979, '18', 'Public access', 5, 'https://barnbougle.com.au/'),
  course('kooyonga-golf-club', 'Kooyonga Golf Club', 'Lockleys', 'Adelaide', 'SA', 138.5287, -34.921, '18', 'Private club', 5, 'https://www.kooyongagolf.com.au/'),
  course('brookwater-golf-country-club', 'Brookwater Golf & Country Club', 'Brookwater', 'Ipswich', 'QLD', 152.8935, -27.658, '18', 'Golf club', 4, 'https://brookwater.com.au/'),
  course('joondalup-resort', 'Joondalup Resort', 'Connolly', 'Perth', 'WA', 115.7467, -31.748, '27', 'Resort course', 4, 'https://www.joondalupresort.com.au/')
];

async function main() {
  let elements = [];
  let source = 'fallback';
  for (const endpoint of endpoints) {
    try {
      const response = await fetch(endpoint, {
        method: 'POST',
        headers: { 'content-type': 'application/x-www-form-urlencoded; charset=UTF-8' },
        body: new URLSearchParams({ data: query })
      });
      if (!response.ok) throw new Error(`${endpoint} returned HTTP ${response.status}`);
      const data = await response.json();
      elements = Array.isArray(data.elements) ? data.elements : [];
      if (elements.length > 50) {
        source = endpoint;
        break;
      }
    } catch (error) {
      console.warn(String(error && error.message ? error.message : error));
    }
  }

  const courses = elements.length ? elements.map(fromOverpass).filter(Boolean) : fallbackCourses;
  const deduped = dedupe(courses).sort((a, b) => a.state.localeCompare(b.state) || a.name.localeCompare(b.name));
  const output = '// Generated by scripts/build-pages-data.mjs from OpenStreetMap/Overpass.\nwindow.GOLF_COURSES = ' + JSON.stringify(deduped, null, 2) + ';\n';
  fs.writeFileSync('courses.js', output, 'utf8');
  console.log(`Generated ${deduped.length} courses from ${source}`);
}

function fromOverpass(element) {
  const tags = element.tags || {};
  const lon = Number(element.lon ?? element.center?.lon);
  const lat = Number(element.lat ?? element.center?.lat);
  if (!Number.isFinite(lon) || !Number.isFinite(lat)) return null;
  const name = clean(tags.name || tags['official_name'] || tags['short_name'] || 'Golf Course near ' + townFromTags(tags));
  const state = clean(tags['addr:state'] || inferState(lon, lat));
  const town = clean(tags['addr:city'] || tags['addr:town'] || tags['addr:suburb'] || tags['addr:locality'] || tags['is_in:city'] || state);
  const region = clean(tags['is_in:region'] || tags['addr:region'] || town || state);
  const website = firstUrl(tags.website || tags['contact:website'] || tags.url || tags['contact:url']);
  const booking = firstUrl(tags.booking || tags['booking:website'] || tags['reservation:website']);
  const id = slug(name + '-' + state + '-' + (element.id || ''));
  const coords = [round(lon), round(lat)];
  const aerial = aerialTile(coords);
  return {
    id,
    name,
    town,
    region,
    state,
    coordinates: coords,
    holes: clean(tags.holes || ''),
    access: accessLabel(tags),
    priceLevel: priceLevel(tags, name),
    homepageUrl: website,
    bookingUrl: booking,
    imageUrl: website ? favicon(website) : aerial,
    fallbackImageUrl: aerial,
    imageAlt: `${name} ${website ? 'logo' : 'aerial course image'}`,
    mediaKind: website ? 'logo' : 'photo',
    summary: summary(tags, town, region, state),
    webSearchUrl: 'https://www.google.com/search?q=' + encodeURIComponent(`${name} ${town} ${state} golf club website`)
  };
}

function course(id, name, town, region, state, lon, lat, holes, access, priceLevel, homepageUrl) {
  const coords = [lon, lat];
  const aerial = aerialTile(coords);
  return { id, name, town, region, state, coordinates: coords, holes, access, priceLevel, homepageUrl, bookingUrl: '', imageUrl: favicon(homepageUrl), fallbackImageUrl: aerial, imageAlt: `${name} logo`, mediaKind: 'logo', summary: `${holes} hole golf course in ${town}, ${state}.`, webSearchUrl: 'https://www.google.com/search?q=' + encodeURIComponent(`${name} ${town} ${state} golf club website`) };
}

function dedupe(items) {
  const seen = new Set();
  const out = [];
  for (const item of items) {
    const key = slug(item.name + '-' + item.state + '-' + item.town);
    if (seen.has(key)) continue;
    seen.add(key);
    out.push(item);
  }
  return out;
}

function accessLabel(tags) {
  const access = String(tags.access || '').toLowerCase();
  if (access === 'private' || tags.private === 'yes') return 'Private club';
  if (access === 'customers') return 'Public access';
  if (access === 'permissive' || access === 'yes' || !access) return 'Golf course';
  return clean(tags.access || 'Golf course');
}

function priceLevel(tags, name) {
  const text = `${name} ${tags.access || ''} ${tags.fee || ''}`.toLowerCase();
  if (/royal|barnbougle|resort|links|private|championship|sanctuary|joondalup|brookwater/.test(text)) return 5;
  if (/country club|club|fee|yes/.test(text)) return 3;
  return 2;
}

function summary(tags, town, region, state) {
  const bits = [];
  if (tags.holes) bits.push(`${tags.holes} holes`);
  if (tags.par) bits.push(`Par ${tags.par}`);
  if (tags.operator) bits.push(`Operated by ${tags.operator}`);
  const prefix = bits.length ? bits.join(' - ') + '. ' : '';
  return prefix + `Golf course in ${[town, region, state].filter(Boolean).join(', ')}.`;
}

function inferState(lon, lat) {
  if (lat < -39) return 'TAS';
  if (lon < 129) return 'WA';
  if (lon >= 129 && lon < 138) return lat > -26 ? 'NT' : 'SA';
  if (lon >= 138 && lon < 141) return lat > -29 ? 'QLD' : 'SA';
  if (lon >= 141 && lon < 144) return lat > -29 ? 'QLD' : lat > -36 ? 'NSW' : 'VIC';
  if (lon >= 144 && lon < 150) return lat > -37 ? 'NSW' : 'VIC';
  if (lon >= 150) return lat > -29 ? 'QLD' : 'NSW';
  return 'AU';
}

function townFromTags(tags) {
  return tags['addr:city'] || tags['addr:town'] || tags['addr:suburb'] || tags['addr:locality'] || tags['is_in:city'] || 'Australia';
}

function firstUrl(value) {
  const text = String(value || '').split(';')[0].trim();
  if (!text) return '';
  return /^https?:\/\//i.test(text) ? text : 'https://' + text.replace(/^\/\//, '');
}

function favicon(value) {
  try {
    const host = new URL(value).hostname;
    return `https://icons.duckduckgo.com/ip3/${host}.ico`;
  } catch {
    return '';
  }
}

function aerialTile(coords) {
  const lon = Number(coords[0]);
  const lat = Number(coords[1]);
  const z = 16;
  const latRad = lat * Math.PI / 180;
  const n = 2 ** z;
  const x = Math.floor((lon + 180) / 360 * n);
  const y = Math.floor((1 - Math.log(Math.tan(latRad) + 1 / Math.cos(latRad)) / Math.PI) / 2 * n);
  return `https://services.arcgisonline.com/ArcGIS/rest/services/World_Imagery/MapServer/tile/${z}/${y}/${x}`;
}

function clean(value) {
  return String(value || '').trim().replace(/\s+/g, ' ');
}
function slug(value) {
  return String(value || 'course').toLowerCase().replace(/&/g, ' and ').replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '').slice(0, 90);
}
function round(value) {
  return Math.round(Number(value) * 1000000) / 1000000;
}

await main();
